package it.molinari.server.model;

public class Ricercato 
{
	String valore;
	int modalita;
	
	public Ricercato()
	{
		
	}

	public String getValore() {
		return valore;
	}

	public void setValore(String valore) {
		this.valore = valore;
	}

	public int getModalita() {
		return modalita;
	}

	public void setModalita(int modalita) {
		this.modalita = modalita;
	}
	
	
}
